    public void handleRegistration() throws SQLException{
        ResultSet resultSet = null;
        Statement statement = db.connectDbb().createStatement();
        String userName =  UserNameValue.getText();
        String selectSql = "SELECT * from Userr where nameUserr = " +"'"+ userName +"'";
        System.out.println(selectSql);
        resultSet = statement.executeQuery(selectSql);
        if(resultSet.next() && resultSet.getString(2).equals(userName)){
            JOptionPane.showMessageDialog(null, "UserName already in use","Registration Error" , JOptionPane.ERROR_MESSAGE );
        }else{
            PreparedStatement ps = db.connectDbb().prepareStatement("Insert into Userr(nameUserr , passWordd) values(?,?)");
            ps.setString(1, userName);
            ps.setString(2, new String(PassWordValue.getPassword()));
            ps.executeUpdate();
        }
        
        
    }
    
