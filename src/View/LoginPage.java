    public void handleLogin() throws SQLException{
        ResultSet resultSet = null;
        Statement statement = db.connectDbb().createStatement();
        String userName =  UserNameValue.getText();
        String pwd = new String(PassWordValue.getPassword());
        String selectSql = "SELECT * from Userr where nameUserr = " +"'"+ userName +"'";
        System.out.println(selectSql);
        resultSet = statement.executeQuery(selectSql);
        Userr us ;
        if(resultSet.next()){
            if(pwd.equals(resultSet.getString(3))){
                us = new Userr(resultSet.getInt(1), resultSet.getString(2)
                        , resultSet.getString(3));
                AccountScreen acp = new AccountScreen(us);
                acp.setTitle("Welcome");
                acp.setVisible(true);
                this.setVisible(false);
            }else{
                JOptionPane.showMessageDialog(null, "PassWord is wrong","Login Error" , JOptionPane.ERROR_MESSAGE );
            }
        }else{
            JOptionPane.showMessageDialog(null, "User not found","Login Error" , JOptionPane.ERROR_MESSAGE );
        }
        UserNameValue.setText("");
        PassWordValue.setText("");
    }
    
    
